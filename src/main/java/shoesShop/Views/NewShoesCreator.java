    private boolean isAddedM(String m) {
    	for(int i=0; i<jTable3.getRowCount(); i++) {
    		String s = (String) jTable3.getValueAt(i, 0);
    		if(m.equals(s))
    			return true;
    	}
    	return false;
    }

